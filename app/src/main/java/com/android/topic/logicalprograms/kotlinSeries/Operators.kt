package com.android.topic.logicalprograms.kotlinSeries

fun main() {

    // Arithmetic Operators
    val i = 14
    val j = 2

    println( i + j )  // 16
    println( i - j )  // 12
    println( i * j )  // 28
    println( i / j )  // 7
    println( i % j )  // 0



    // Relational Operators
    println( i > j )  // true
    println( i < j )  // false

    println( i >= j )  // true
    println( i <= j )  // false

    println( i == j )  // false
    println( i != j )  // true
}