package com.android.topic.logicalprograms.kotlinSeries

fun main() {

    // Var can be reassigned
    var point = 5
    println(point)

    // Reassigned point
    point = 10
    println(point)



    // Val can't be reassigned
    val rate = 5
    println(rate)


    // Try to reassigned rate
    //rate = 15  //  When you uncomment this it will give you compiler error
    println(rate)


}